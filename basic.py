# Python program to print "Hello, World!" and calculate the sum of numbers from 1 to 10

# Print "Hello, World!"
print("Hello, World!")

# Initialize the sum variable
total_sum = 0

# Calculate the sum of numbers from 1 to 10
for number in range(1, 11):
    total_sum += number

# Print the result
print("The sum of numbers from 1 to 10 is:", total_sum)
